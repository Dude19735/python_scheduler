"""
Module contains definition of UnitTypes
"""

class UnitTypes():

    """
    Class defines different types of work units. Every type corresponds
    to one start-time-button (ie. work, break, coffee...)
    """

    INIT_TIME = 0
    WORK_TIME = 1
    BREAK_TIME = 2
    COFFEE_TIME = 3
    SCHOOL_TIME = 4
